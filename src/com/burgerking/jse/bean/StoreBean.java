package com.burgerking.jse.bean;

public class StoreBean {
	private String food,hamburg,coke,chip;
	private int price,takeout,money;
	
	public String getFood() {
		return food;
	}
	public void setFood(String food) {
		this.food = food;
	}
	public String getHamburg() {
		return hamburg;
	}
	public void setHamburg(String hamburg) {
		this.hamburg = hamburg;
	}
	public String getCoke() {
		return coke;
	}
	public void setCoke(String coke) {
		this.coke = coke;
	}
	public String getChip() {
		return chip;
	}
	public void setChip(String chip) {
		this.chip = chip;
	}
	public int getPrice() {
		return price;
	}
	public void setPrice(int price) {
		this.price = price;
	}
	public int getTakeout() {
		return takeout;
	}
	public void setTakeout(int takeout) {
		this.takeout = takeout;
	}
	public int getMoney() {
		return money;
	}
	public void setMoney(int money) {
		this.money = money;
	}
}
